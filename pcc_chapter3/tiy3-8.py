places = ['hokkaido', 'okinawa', 'germany', 'australia', 'argentina', 'new zealand', 'taiwan', 'madagascar']
print(places)

#temp abc sorting
print(sorted(places))
print(places)

#temp reverse abc sorting
print(sorted(places, reverse=True))
print(places)

#reverse order
places.reverse()
print(places)

#reverse the reverse
places.reverse()
print(places)

#permanent abc sort
places.sort()
print(places)

#permanent reverse abc sort
places.sort(reverse=True)
print(places)