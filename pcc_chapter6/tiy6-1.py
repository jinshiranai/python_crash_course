# PCC Try It Yourself 6-1. Person

person = {
    'first': 'jane',
    'last': 'doe',
    'age': '27',
    'location': 'unknown',
    'threat level': 'severe',
    }

print(f"First Name: {person['first'].title()}")
print(f"Last Name: {person['last'].title()}")
print(f"Age: {person['age'].title()}")
print(f"Location: {person['location'].title()}")
print(f"Threat Level: {person['threat level'].upper()}")